class ComoChegar {
  late String carro;
  late String jipe;
  late String barco;

  ComoChegar(this.carro, this.jipe, this.barco);

  void updateCarro(String novoCarro) {
    carro = novoCarro;
  }

  void updateJipe(String novoJipe) {
    jipe = novoJipe;
  }

  void updateBarco(String novoBarco) {
    barco = novoBarco;
  }
}
